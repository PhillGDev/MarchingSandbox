    IEnumerator Populate()
    {
        // The data points for terrain are stored at the CORNERS of our "cubes", so the terrainMap needs to be 1 larger
        // than the width/height of our mesh.
        //Also check to see if this terrain has edits.
        if (SavedData.Exists)
        {
            if (SavedData.Modifcations.Count != 0)
            {
                Modified = new Dictionary<int3, float>();
                Debug.Log("Loading Modified nodes. " + SavedData.Modifcations.Count + " Modified.");
                foreach (KeyValuePair<int3, float> Pair in SavedData.Modifcations)
                {
                    Modified.Add(Pair.Key, Pair.Value);
                }
                for (int x = 0; x < size + 1; x++)
                {
                    //Debug.Log("Populating Map.. " + x + " / " + (width + 1));
                    for (int z = 0; z < size + 1; z++)
                    {
                        for (int y = 0; y < size + 1; y++)
                        {
                            int3 coordinate = new int3(x, y, z);
                            if (SavedData.Modifcations.ContainsKey(coordinate))
                            {
                                float val = Mathf.NegativeInfinity;
                                SavedData.Modifcations.TryGetValue(coordinate, out val);
                                if (val == Mathf.NegativeInfinity) Debug.LogError("Failed to write a terrain node because it doesn't exist, even though it was contained within the modifcations list. You should not be able to receive this error.");
                                terrainMap[x, y, z] = val;
                            }
                            else
                            {
                                //Scale for current = 25.
                                int3 Coord = new int3(x, y, z);
                                terrainMap[x, y, z] = ChunkCreator.Singleton.CalculateDensity(Coord, NoiseOffset, size);
                            }
                        }
                    }
                }
            }
            else
            {
                for (int x = 0; x < size + 1; x++)
                {
                    //Debug.Log("Populating Map.. " + x + " / " + (width + 1));
                    for (int z = 0; z < size + 1; z++)
                    {
                        for (int y = 0; y < size + 1; y++)
                        {
                            int3 Coord = new int3(x, y, z);
                            terrainMap[x, y, z] = ChunkCreator.Singleton.CalculateDensity(Coord, NoiseOffset, size);
                        }
                    }
                }
            }
        }
        else //If not, run a different loop (this should be more efficent)
        {
            for (int x = 0; x < size + 1; x++)
            {
                //Debug.Log("Populating Map.. " + x + " / " + (width + 1));
