	Vector3[,,] GetVecs(int Res)
	{
		Vector3[,,] pos = new Vector3[Res, Res, Res];
		float Size = (float)Res / size;
		for (int x = 0; x < Res; x++)
		{
			for (int y = 0; y < Res; y++)
			{
				for (int z = 0; z < Res; z++)
				{
					Vector3 point = new Vector3(x, y, z) * Size;
					pos[x, y, z] = transform.position + point;
				}
			}
		}
		return pos;
	}
	public Vector3 GetClosestPoint(Vector3 point)
	{
		Vector3[,,] ToCheck = GetVecs(3);
		//if we only have 3 points per axis, then we'll only need to check 27 things. instead of an insane 4,096 things.
		float MinDistance = Mathf.Infinity;
		Vector3 ClosestPoint = Vector3.positiveInfinity;
		for (int x = 0; x < 3; x++)
		{
			for (int y = 0; y < 3; y++)
			{
				for (int z = 0; z < 3; z++)
				{
					float dist = Vector3.Distance(ToCheck[x, y, z], point);
					if (MinDistance > dist)
					{
						MinDistance = dist;
						ClosestPoint = ToCheck[x, y, z];
					}
				}
			}
		}
		return ClosestPoint;
	}
