    IEnumerator CreateMesh() //This takes a bit.
    {

        ClearMeshData();

        // Loop through each "cube" in our terrain.
        int WaitSize = 20; //Do 2 * size calc per frame. (at 60fps, that's 32 * MarchCube per 0.0166666666666667 seconds)
        for (int x = 0; x < size; x++)
        {
            for (int y = 0; y < size; y++)
            {
                //Debug.Log("Creating Mesh.. " + y + " / " + ((width + 1) * width));
                for (int z = 0; z < size; z++)
                {
                    // Pass the value into our MarchCube function.
                    MarchCube(new Vector3Int(x, y, z));
                }
            }
            WaitSize--;
            if (WaitSize == 0)
            {
                yield return null;
                WaitSize = 20;
            }
            //Debug.ClearDeveloperConsole();
        }
        BuildMesh();
        running = null;
    }

