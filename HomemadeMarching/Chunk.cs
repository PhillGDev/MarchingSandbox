	IEnumerator RebuildMesh()
	{
		if (!awaitingGen)
		{
			ChunkCreator.Singleton.ToGenerate.Enqueue(this);
			awaitingGen = true;
		}
		while (ChunkCreator.Singleton.ToGenerate.Peek() != this) yield return null;
		StartCoroutine(CreateMesh());
		awaitingGen = false;
		ChunkCreator.Singleton.ToGenerate.Dequeue();
	}
	IEnumerator CreateMesh() //This takes a bit.
	{

		ClearMeshData();

		// Loop through each "cube" in our terrain.
		int WaitSize = 20; //Do 2 * size calc per frame. (at 60fps, that's 32 * MarchCube per 0.0166666666666667 seconds)
		for (int x = 0; x < size; x++)
		{
			for (int y = 0; y < size; y++)
			{
				//Debug.Log("Creating Mesh.. " + y + " / " + ((width + 1) * width));
				for (int z = 0; z < size; z++)
				{
					// Pass the value into our MarchCube function.
					MarchCube(new Vector3Int(x, y, z));
				}
			}
			WaitSize--;
			if (WaitSize == 0)
			{
				yield return null;
				WaitSize = 20;
			}
			//Debug.ClearDeveloperConsole();
		}
		BuildMesh();
		running = null;
	}

	Coroutine running;
	IEnumerator Populate()
	{
		// The data points for terrain are stored at the CORNERS of our "cubes", so the terrainMap needs to be 1 larger
		// than the width/height of our mesh.
		for (int x = 0; x < size + 1; x++)
		{
			//Debug.Log("Populating Map.. " + x + " / " + (width + 1));
			for (int z = 0; z < size + 1; z++)
			{
				for (int y = 0; y < size + 1; y++)
				{
					Vector3 Position = ChunkCreator.Singleton.Offset + NoiseOffset + new Vector3((float)x / size + 1, (float)y / size + 1, (float)z / size + 1);
					//Cutoff beginning at the midpoint of the chunk.
					Vector3 point = ChunkCreator.Singleton.Offset + NoiseOffset + new Vector3((float)x / (float)size + 1 * Scale, (float)y / (float)size + 1 * Scale, (float)z / (float)size + 1 * Scale);
					if (Position.y >= 0f)
					{
						//Terrain below the perlin noise heighmap are controlled by caves.
						//We can just do this with Sine.
						//Show the 3D Perlin where the mask is.
						//0-1
						//1 -> Empty
						//0 - Terrain.
						float value = (Position.y - ChunkCreator.Singleton.SurfaceLevel) * .2f;
						value = Mathf.Clamp01(value);
						value -= Mathf.PerlinNoise(point.x, point.z);
						terrainMap[x, y, z] = Mathf.Lerp(Perlin3Noise(point), 1f, value);
						//Debug.Log(Position.y);
						//Creating ores are simple, just check to see if this noise value is higher than the ore threshold.
					}
					else terrainMap[x, y, z] = Perlin3Noise(point);
				}
			}
		}
		yield return null;
		awaitingGen = false;
		running = null;
	}
	float Perlin3Noise(Vector3 Point)
	{
		float AB = Mathf.PerlinNoise(Point.x, Point.y);
		float BC = Mathf.PerlinNoise(Point.y, Point.z);
		float AC = Mathf.PerlinNoise(Point.x, Point.z);
		float BA = Mathf.PerlinNoise(Point.y, Point.x);
		float CB = Mathf.PerlinNoise(Point.z, Point.y);
		float CA = Mathf.PerlinNoise(Point.z, Point.x);
		return (AB + BC + AC + BA + CB + CA) / 6f;
	}
	void MarchCube(Vector3Int position)
	{
		Vector3 WorldPos = ChunkCreator.Singleton.Offset + NoiseOffset + new Vector3((float)position.x / size + 1, (float)position.y / size + 1, (float)position.z / size + 1);
		// Sample terrain values at each corner of the cube.
		float[] cube = new float[8];
		for (int i = 0; i < 8; i++)
		{
			cube[i] = SampleTerrain(position + CornerTable[i]);
		}

		// Get the configuration index of this cube.
		int configIndex = GetCubeConfiguration(cube);

		// If the configuration of this cube is 0 or 255 (completely inside the terrain or completely outside of it) we don't need to do anything.
		if (configIndex == 0 || configIndex == 255)
			return;

		// Loop through the triangles. There are never more than 5 triangles to a cube and only three vertices to a triangle.
		int edgeIndex = 0;
		for (int i = 0; i < 5; i++)
		{
			for (int p = 0; p < 3; p++)
			{
				// Get the current indice. We increment triangleIndex through each loop.
				int indice = TriangleTable[configIndex, edgeIndex];

				// If the current edgeIndex is -1, there are no more indices and we can exit the function.
				if (indice == -1)
					return;

				// Get the vertices for the start and end of this edge.
				Vector3 vert1 = position + CornerTable[EdgeIndexes[indice, 0]];
				Vector3 vert2 = position + CornerTable[EdgeIndexes[indice, 1]];

				Vector3 vertPosition;
				if (smoothTerrain)
				{

					// Get the terrain values at either end of our current edge from the cube array created above.
					float vert1Sample = cube[EdgeIndexes[indice, 0]];
					float vert2Sample = cube[EdgeIndexes[indice, 1]];

					// Calculate the difference between the terrain values.
					float difference = vert2Sample - vert1Sample;

					// If the difference is 0, then the terrain passes through the middle.
					if (difference == 0)
						difference = terrainSurface;
					else
						difference = (terrainSurface - vert1Sample) / difference;

					// Calculate the point along the edge that passes through.
					vertPosition = vert1 + ((vert2 - vert1) * difference);


				}
				else
				{

					// Get the midpoint of this edge.
					vertPosition = (vert1 + vert2) / 2f;

				}

				// Add to our vertices and triangles list and incremement the edgeIndex.
				if (flatShaded)
				{
					vertices.Add(vertPosition);
					triangles.Add(vertices.Count - 1);

				}
				else
				{
					triangles.Add(VertForIndice(vertPosition));
				}

				edgeIndex++;

			}
		}

	}
	int GetCubeConfiguration(float[] cube)
	{

		// Starting with a configuration of zero, loop through each point in the cube and check if it is below the terrain surface.
		int configurationIndex = 0;
		for (int i = 0; i < 8; i++)
		{

			// If it is, use bit-magic to the set the corresponding bit to 1. So if only the 3rd point in the cube was below
			// the surface, the bit would look like 00100000, which represents the integer value 32.
			if (cube[i] > terrainSurface)
				configurationIndex |= 1 << i;

		}

		return configurationIndex;

	}
	public void Modifcation(Vector3 Pos, float Radius, bool subtract)
	{
		for (int x = 0; x < size + 1; x++)
		{
			//Debug.Log("Populating Map.. " + x + " / " + (width + 1));
			for (int z = 0; z < size + 1; z++)
			{
				for (int y = 0; y < size + 1; y++)
				{
					float Distance = Vector3.Distance(transform.position + new Vector3(x, y, z), Pos);
					float Effect = -Mathf.Clamp(Distance - Radius, -1f, 0f);
					if (Effect > 0f) Debug.DrawLine(transform.position + new Vector3(x, y, z), Pos, Color.blue);
					//Only positive when the distance is less than the radius.
					//Object reference not set to an instance of an object
					//What?
					//Debug.Log(Effect); Mostly 0, but we get close to 1 sometimes.

					terrainMap[x, y, z] = subtract ? Mathf.Lerp(terrainMap[x, y, z], 0f, Effect) : Mathf.Lerp(terrainMap[x, y, z], 1f, Effect);
				}
			}
		}
		awaitingGen = false;
		StopAllCoroutines();
		running = StartCoroutine(RebuildMesh());
	}

	float SampleTerrain(Vector3Int point)
	{

		return terrainMap[point.x, point.y, point.z];

	}

	int VertForIndice(Vector3 vert)
	{

		// Loop through all the vertices currently in the vertices list.
		for (int i = 0; i < vertices.Count; i++)
		{

			// If we find a vert that matches ours, then simply return this index.
			if (vertices[i] == vert)
				return i;

		}

		// If we didn't find a match, add this vert to the list and return last index.
		vertices.Add(vert);
		return vertices.Count - 1;

	}

