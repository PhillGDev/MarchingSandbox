	IEnumerator Generate()
	{
		triangles = new List<int>();
		vertices = new List<Vector3>();
		while (ChunkCreator.Singleton.ToGenerate.Peek() != this) yield return null;
		//Debug.Log(ChunkCreator.Singleton.ToGenerate.Peek());
		//Debug.Log("Beginning Generation " + Time.time);
		meshFilter = GetComponent<MeshFilter>();
		meshCollider = GetComponent<MeshCollider>();
		transform.tag = "Terrain";
		terrainMap = new float[size + 1, size + 1, size + 1];
		//Debug.Log("Populating Terrain...");
		running = StartCoroutine(Populate());
		while (running != null)  yield return null;
		//Debug.Log("Creating Mesh...");
		running = StartCoroutine(CreateMesh());
		while (running != null) yield return null;
		//Only Create the Items AFTER we created the mesh. (So LOS Checks work)
		for (int i = 0; i < SavedData.Items.Count; i++)
		{
			GameObject prefabobj = LODItemDatabase.Singleton.GetPrefabFromID(SavedData.Items[i].ItemId);
			if (prefabobj == null) { Debug.LogWarning("'" + SavedData.Items[i].ItemId + "' Has no match in LODItemDatabase, Consider removing this item."); continue; }
			Vector3 position, euler;
			SavedData.Items[i].Position.MakeVec(SavedData.Items[i].Position, out position);
			SavedData.Items[i].Euler.MakeVec(SavedData.Items[i].Euler, out euler);
			Instantiate(prefabobj, position, Quaternion.Euler(euler), transform);
			Debug.Log("Loaded " + SavedData.Items[i].ItemId);
		}
		ChunkCreator.Singleton.ToGenerate.Dequeue();
		Dispose();
	}
